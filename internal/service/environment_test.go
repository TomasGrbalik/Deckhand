func TestUp_TemplateRenderingFails(t *testing.T) {
	dir := t.TempDir()
	source := &fakeSource{err: errors.New("template broken")}
	compose := &spyCompose{}
	project := domain.Project{Name: "myapp", Template: "base"}
	svc := service.NewEnvironmentService(source, compose, project, dir)

	err := svc.Up(false)
	if err == nil {
		t.Fatal("expected error when template rendering fails")
	}
	if !strings.Contains(err.Error(), "template broken") {
		t.Errorf("error should contain cause, got: %v", err)
	}

	// No files should be written.
	if _, statErr := os.Stat(filepath.Join(dir, ".deckhand")); !os.IsNotExist(statErr) {
		t.Error(".deckhand/ should not exist when rendering fails")
	}

	// ComposeRunner should not be called.
	if len(compose.upCalls) != 0 {
		t.Error("ComposeRunner.Up should not be called when rendering fails")
	}
}

