func TestUp_RendersAndStartsContainers(t *testing.T) {
	svc, compose, dir := newTestEnv(t)

	if err := svc.Up(false); err != nil {
		t.Fatalf("Up() error: %v", err)
	}

	// Verify .deckhand/ directory was created.
	deckhandDir := filepath.Join(dir, ".deckhand")
	info, err := os.Stat(deckhandDir)
	if err != nil {
		t.Fatalf(".deckhand/ not created: %v", err)
	}
	if !info.IsDir() {
		t.Fatal(".deckhand is not a directory")
	}

	// Verify Dockerfile was written with header.
	dockerfile, err := os.ReadFile(filepath.Join(deckhandDir, "Dockerfile"))
	if err != nil {
		t.Fatalf("reading Dockerfile: %v", err)
	}
	if !strings.HasPrefix(string(dockerfile), "# Generated by deckhand — DO NOT EDIT") {
		t.Errorf("Dockerfile missing DO NOT EDIT header\nGot:\n%s", dockerfile)
	}
	if !strings.Contains(string(dockerfile), "Project: myapp") {
		t.Errorf("Dockerfile missing rendered content\nGot:\n%s", dockerfile)
	}

	// Verify docker-compose.yml was written with header.
	composeYAML, err := os.ReadFile(filepath.Join(deckhandDir, "docker-compose.yml"))
	if err != nil {
		t.Fatalf("reading docker-compose.yml: %v", err)
	}
	if !strings.HasPrefix(string(composeYAML), "# Generated by deckhand — DO NOT EDIT") {
		t.Errorf("docker-compose.yml missing DO NOT EDIT header\nGot:\n%s", composeYAML)
	}

	// Verify ComposeRunner.Up was called correctly.
	if len(compose.upCalls) != 1 {
		t.Fatalf("expected 1 Up call, got %d", len(compose.upCalls))
	}
	call := compose.upCalls[0]
	if call.projectDir != dir {
		t.Errorf("Up projectDir = %q, want %q", call.projectDir, dir)
	}
	expectedComposePath := filepath.Join(deckhandDir, "docker-compose.yml")
	if call.composePath != expectedComposePath {
		t.Errorf("Up composePath = %q, want %q", call.composePath, expectedComposePath)
	}
	if call.build {
		t.Error("Up build = true, want false")
	}
}

