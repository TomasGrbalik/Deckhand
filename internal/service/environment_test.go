// spyCompose records calls to ComposeRunner methods.
type spyCompose struct {
	upCalls      []composeCall
	downCalls    []composeCall
	destroyCalls []composeCall
	upErr        error
	downErr      error
	destroyErr   error
}

type composeCall struct {
	projectDir  string
	composePath string
	build       bool // only relevant for Up
}

func (f *spyCompose) Up(projectDir, composePath string, build bool) error {
	f.upCalls = append(f.upCalls, composeCall{projectDir, composePath, build})
	return f.upErr
}

func (f *spyCompose) Down(projectDir, composePath string) error {
	f.downCalls = append(f.downCalls, composeCall{projectDir, composePath, false})
	return f.downErr
}

func (f *spyCompose) Destroy(projectDir, composePath string) error {
	f.destroyCalls = append(f.destroyCalls, composeCall{projectDir, composePath, false})
	return f.destroyErr
}

// spyVolumeManager records calls to VolumeManager methods.
type spyVolumeManager struct {
	listResult     []service.VolumeInfo
	listErr        error
	listedProjects []string
	removed        []string
	removeErr      error
}

func (f *spyVolumeManager) ListByProject(project string) ([]service.VolumeInfo, error) {
	f.listedProjects = append(f.listedProjects, project)
	return f.listResult, f.listErr
}

func (f *spyVolumeManager) Remove(name string) error {
	f.removed = append(f.removed, name)
	return f.removeErr
}

