func TestUp_OverwritesPreviousFiles(t *testing.T) {
	svc, _, dir := newTestEnv(t)

	deckhandDir := filepath.Join(dir, ".deckhand")
	if err := os.MkdirAll(deckhandDir, 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(filepath.Join(deckhandDir, "Dockerfile"), []byte("stale"), 0o644); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(filepath.Join(deckhandDir, "docker-compose.yml"), []byte("stale"), 0o644); err != nil {
		t.Fatal(err)
	}

	if err := svc.Up(false); err != nil {
		t.Fatalf("Up() error: %v", err)
	}

	dockerfile, _ := os.ReadFile(filepath.Join(deckhandDir, "Dockerfile"))
	if strings.Contains(string(dockerfile), "stale") {
		t.Error("Dockerfile was not overwritten")
	}
	if !strings.Contains(string(dockerfile), "Project: myapp") {
		t.Error("Dockerfile missing fresh rendered content")
	}
}

func TestUp_DONOTEDITHeaderFormat(t *testing.T) {
	svc, _, dir := newTestEnv(t)

	if err := svc.Up(false); err != nil {
		t.Fatalf("Up() error: %v", err)
	}

	expectedHeader := "# Generated by deckhand — DO NOT EDIT\n# Source: .deckhand.yaml\n# Regenerate with: deckhand up\n"

	for _, name := range []string{"Dockerfile", "docker-compose.yml"} {
		content, err := os.ReadFile(filepath.Join(dir, ".deckhand", name))
		if err != nil {
			t.Fatalf("reading %s: %v", name, err)
		}
		if !strings.HasPrefix(string(content), expectedHeader) {
			t.Errorf("%s has wrong header.\nWant prefix:\n%s\nGot:\n%s", name, expectedHeader, content)
		}
	}
}

func TestDown_StopsContainersKeepsFiles(t *testing.T) {
	svc, compose, dir := newTestEnv(t)

	// First create some files via Up.
	if err := svc.Up(false); err != nil {
		t.Fatalf("Up() error: %v", err)
	}

	if err := svc.Down(); err != nil {
		t.Fatalf("Down() error: %v", err)
	}

	// Verify ComposeRunner.Down was called.
	if len(compose.downCalls) != 1 {
		t.Fatalf("expected 1 Down call, got %d", len(compose.downCalls))
	}
	call := compose.downCalls[0]
	if call.projectDir != dir {
		t.Errorf("Down projectDir = %q, want %q", call.projectDir, dir)
	}

	// Verify .deckhand/ still exists.
	if _, err := os.Stat(filepath.Join(dir, ".deckhand")); err != nil {
		t.Errorf(".deckhand/ should still exist after Down: %v", err)
	}
}

